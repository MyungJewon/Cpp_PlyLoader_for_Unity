#pragma once
#include <string>
#include "PointCloud.h"

// ============================================================
// PlyParser.h — PLY 파일 파서의 "명세서(선언부)"
// ============================================================
// 유니티로 치면 인터페이스(interface)나 함수 시그니처 목록 같은 역할입니다.
//
// [헤더 파일(.h)이 왜 필요한가?]
// C++은 코드를 위에서 아래로 순서대로 읽습니다.
// main.cpp에서 loadPly() 함수를 쓰려면,
// 컴파일러가 그 함수의 "생김새(이름, 파라미터, 반환값)"를 미리 알아야 합니다.
//
// 헤더 파일은 "이런 함수가 어딘가에 존재한다"고 컴파일러에게 미리 알려주는 문서입니다.
// 실제 구현(내용)은 PlyParser.cpp에 있고,
// 이 .h 파일은 그 함수의 껍데기(선언)만 갖고 있습니다.
//
// main.cpp에서 #include "PlyParser.h" 한 줄만 쓰면
// "loadPly라는 함수가 있구나" 하고 컴파일러가 인식하게 됩니다.
// ============================================================

// PLY 파일을 읽어서 PointCloud 구조체에 데이터를 채워주는 함수
//
// [파라미터 설명]
// const std::string& filePath
//   → 읽을 파일의 경로. '&'는 참조(reference)라는 뜻으로,
//     복사 없이 원본을 그대로 전달합니다. (유니티의 ref 키워드와 비슷)
//     'const'는 이 함수 안에서 filePath를 절대 수정하지 않겠다는 약속입니다.
//
// PointCloud& cloud
//   → 데이터를 채워 넣을 PointCloud 구조체. '&'이므로 원본이 수정됩니다.
//     (유니티의 out 키워드와 비슷한 개념)
//
// [반환값]
// bool → 파일을 성공적으로 읽으면 true, 실패하면 false
bool loadPly(const std::string& filePath, PointCloud& cloud);
