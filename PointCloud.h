#pragma once
#include <vector>

// ============================================================
// PointCloud.h — 포인트 클라우드 "데이터 보관함"
// ============================================================
// 유니티로 치면 ScriptableObject랑 비슷한 역할입니다.
// 렌더링 방법도, 파일 읽는 방법도 모르고
// 그냥 "데이터만" 들고 있는 순수한 구조체입니다.
//
// [struct vs class]
// C++에서 struct와 class는 거의 같습니다.
// 차이는 딱 하나 — struct는 기본이 public(누구나 접근 가능),
//                    class는 기본이 private(본인만 접근 가능).
// 데이터를 담기만 하는 용도라면 struct가 관례상 더 자연스럽습니다.
// ============================================================

struct PointCloud {

    // --- 날것의 바이트 데이터 ---
    // PLY 파일에서 읽어온 데이터를 1바이트(unsigned char) 단위로 통째로 보관합니다.
    // (유니티의 byte[] 배열과 똑같은 개념)
    // 왜 그냥 float 배열로 안 쓰냐고요?
    // PLY 파일 안에는 좌표(float)도 있고, 색상(uchar)도 섞여 있어서
    // 가장 작은 단위인 1바이트로 저장해두고 필요할 때마다 꺼내 씁니다.
    std::vector<unsigned char> rawData;

    // --- 헤더에서 읽어온 파일 구조 정보 ---

    // 총 점의 개수 (예: 1,200,000개)
    // = 0으로 초기화 (C++에서 = 0은 기본값 지정)
    int vertexCount = 0;

    // 점 1개가 rawData 안에서 차지하는 바이트 수
    // 예) x(4) + y(4) + z(4) + nx(4) + ny(4) + nz(4) + r(1) + g(1) + b(1) = 27바이트
    // 다음 점으로 건너뛸 때 이 값만큼 이동하면 됩니다.
    int stride = 0;

    // rawData 안에서 색상(빨간색 R) 데이터가 시작되는 위치 (바이트 번호)
    // -1이면 이 파일에는 색상 데이터가 없다는 뜻입니다.
    int colorOffset = -1;

    // --- 카메라가 자동으로 맞춰보기 위한 계산값 ---

    // 전체 점들의 평균 위치 (무게중심)
    // 카메라가 이 지점을 바라보도록 조정합니다.
    float centerX = 0.0f;
    float centerY = 0.0f;
    float centerZ = 0.0f;

    // 카메라 투영 상자의 크기
    // 가장 멀리 있는 점을 기준으로 자동 계산됩니다.
    float orthoSize = 15.0f;
};
